	 public int ReturnIndex(String value) {
		 String [] name = {"TO DO", "IN PROGRESS","TO BE REVISED", "DONE"};
		 
		 for(int i = 0; i<name.length; i++) {
			 if(name[i].equals(value))
				 return i;
		 }
		return -1;
	 }
	 
