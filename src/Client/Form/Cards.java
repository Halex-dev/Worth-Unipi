    private void MoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    	String value = ComboCards.getSelectedItem().toString();
    	
    	int chosen = ReturnIndex(value);
    	if(Selected >= 0) {
	    	if(chosen >= 0 && GUIClientMain.MoveCards(this.name, data[Selected], this.where, chosen)) {
	    		GUIClientMain.ShowCards(this.name, this.where);
	    	}
	    	else {
	    		JOptionPane.showMessageDialog(this, "Something went wrong");
	    		GUIClientMain.ShowCards(this.name, this.where);
	    	}
    	}
    	else {
    		JOptionPane.showMessageDialog(this, "Select a row first");
    	}
    }

